#include "libft.h"

void	ft_putnbr(int n)
{
	if (n > -10 && n < 10)
	{
		if (n > -10 && n < 0)
		{
			ft_putchar('-');
			ft_putchar(-1 * (n % 10 - '0'));
		}
		else
			ft_putchar(n % 10 + '0');
	}
	else
	{
		ft_putnbr(n / 10);
		if (n >= 0)
			ft_putchar(n % 10 + '0');
		else
			ft_putchar(-1 * (n % 10 - '0'));
	}
}
